"""벌집"""

'''
1  -> 1개
7  -> 2개  1+6  : 1+6  :6*1
19 -> 3개 7+12  : 1+18 :6*3
37 -> 4개 19+18 : 1+36 :6*6
61 -> 5개 37+24 : 1+60 :6*10


1 -> 1개 : 1
2 -> 2개 : 1+1
8 -> 3개 : 2+6
20 -> 4개 : 8+12
38 -> 5개


    '''
n = int(input())

nums_pileup = 1  # 벌집의 개수, 1개부터 시작
cnt = 1

while n > nums_pileup:
    nums_pileup += 6 * cnt  # 벌집이 6의 배수로 증가
    cnt += 1
print(cnt)